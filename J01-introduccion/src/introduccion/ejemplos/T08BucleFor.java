package introduccion.ejemplos;

public class T08BucleFor {
	public static void main(String[] arg) {
		int cont =1;
		while (cont<=50) {
			System.out.println(cont);
			cont++;
			
			}
		System.out.println("-------------------------");
		
		for(int i = 1; i <=50 ; i++) {
			System.out.println(i);
		}
		System.out.println("-------------------------");
		
		for(int i = 10; i >=0 ; i--) {
			System.out.println(i);
		}
	}	
}


