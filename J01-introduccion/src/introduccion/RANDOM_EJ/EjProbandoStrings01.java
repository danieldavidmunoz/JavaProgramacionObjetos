package introduccion.RANDOM_EJ;

public class EjProbandoStrings01 {
    public static void printUntilNumber(int number) {
        int contador=1;
        while (number>=contador) {
            System.out.println(contador);
            contador++;
                    }
    
    }
    
    public static void main(String[] args) {
        printUntilNumber(5); 
    }
   

}
